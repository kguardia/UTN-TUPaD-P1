# Cálculo del área de un círculo
radio = 6
pi = 3.14159
area = pi * radio * radio

# Impresión del resultado
print("El área del círculo es:", area)

# Otro ejemplo:
# Calcular la edad del usuario pidiendo año de nacimiento dado que estamos en el 2024.
birth_year = int(input("Ingrese su año de nacimiento "))
actual_year = 2025

age = actual_year - birth_year
print(f"Su edad es: {age}")
